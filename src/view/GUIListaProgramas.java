    private void actualizar()
    {
          tabla.setRowCount(0);
  
        try 
        {
        model.ServicioUniversidadSW_Service service = new model.ServicioUniversidadSW_Service();
        model.ServicioUniversidadSW port = service.getServicioUniversidadSWPort();
        List<Object> est = port.listarProgramas();
        model.ProgramaAcademico[] estudiantes = est.stream().toArray(model.ProgramaAcademico[]::new);
        
        for(int i=0; i<estudiantes.length;i++)
        {
              model.ProgramaAcademico estu = (model.ProgramaAcademico)estudiantes[i];
              String nombre = estu.getNombre();
              String codigo = estu.getCodigo();
              String modalidad = estu.getModalidad();
              String duracion = String.valueOf(estu.getDuracionMeses());
              String registro = estu.getRegistroCalificado();
              
                       
       
              tabla.addRow(new Object[]{nombre, codigo, registro, modalidad, duracion});
        }
        
       }
             
         catch (Exception ex) {
            Logger.getLogger(GUIListar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
